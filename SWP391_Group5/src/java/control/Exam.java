    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
   protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        String examIdParam = request.getParameter("id");
        String courseIdParam = request.getParameter("courseId");
        dao dao = new dao();
        if (examIdParam != null && courseIdParam != null) {
            int examId = Integer.parseInt(examIdParam);
            int courseId = Integer.parseInt(courseIdParam);
            List<ExamQuestion> questionList = dao.getExamQuestionsByExamId(examId);
            request.setAttribute("examId", examId);
            request.setAttribute("courseId", courseId);
            request.setAttribute("questions", questionList);
            request.getRequestDispatcher("Exam.jsp").forward(request, response);
        } else {
            response.sendRedirect("index.jsp"); // Redirect to a default page if no exam ID is provided
        }
    }

